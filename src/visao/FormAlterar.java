    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
        // TODO add your handling code here:
        try{
            Autor a = new Autor();
            AutorDAO ad = new AutorDAO();
            a.setNum(Integer.valueOf(jTnum.getText()));
            ad.PesquisarRegistro(a);
            jTnome.setText(a.getNome());
            //jTIdade.setColumns(Integer.parseInt(txdoc.getText()));
            jTgen.setText(String.valueOf(a.getGen()));
            jTemail.setText(a.getEmail());
            jTnum.setEditable(false);

        }
        catch (Exception e){
            System.out.println("Erro " + e.getMessage());
        }
    }//GEN-LAST:event_jBPesquisarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        try{

            Autor a = new Autor();
            AutorDAO ad = new AutorDAO();

            a.setNum(Integer.valueOf(jTnum.getText()));
            a.setNome(jTnome.getText());
            a.setGen(jTgen.getText());
            a.setEmail(jTemail.getText());
            ad.editarAutor(a);

            jTnome.setText("");
            jTemail.setText("");
            jTgen.setText("");
            jTnum.setText("");
            jTnum.requestFocus();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Erro de : " + e.getMessage());
        }

    }//GEN-LAST:event_jBEditarActionPerformed

